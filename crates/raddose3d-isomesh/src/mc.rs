/// Lorensen-Cline Marching Cubes.
///
/// Uses the canonical 256-entry edge table and triangle table (Paul Bourke layout).
/// Output coordinates are in the same µm space as the input grid.
use crate::csv::Grid;

pub struct Mesh {
    pub verts: Vec<[f32; 3]>,
    pub tris: Vec<[u32; 3]>,
}

pub fn marching_cubes(grid: &Grid, iso: f32) -> Mesh {
    let nx = grid.nx;
    let ny = grid.ny;
    let nz = grid.nz;
    let s = grid.spacing;
    let min = grid.min;

    let mut verts: Vec<[f32; 3]> = Vec::new();
    let mut tris: Vec<[u32; 3]> = Vec::new();

    let sample = |i: usize, j: usize, k: usize| -> f32 {
        if i < nx && j < ny && k < nz {
            grid.data[i * ny * nz + j * nz + k]
        } else {
            0.0
        }
    };

    let pos = |i: usize, j: usize, k: usize| -> [f32; 3] {
        [
            min[0] + i as f32 * s,
            min[1] + j as f32 * s,
            min[2] + k as f32 * s,
        ]
    };

    let interp = |p0: [f32; 3], p1: [f32; 3], v0: f32, v1: f32| -> [f32; 3] {
        if (v1 - v0).abs() < 1e-10 {
            return p0;
        }
        let t = (iso - v0) / (v1 - v0);
        [
            p0[0] + t * (p1[0] - p0[0]),
            p0[1] + t * (p1[1] - p0[1]),
            p0[2] + t * (p1[2] - p0[2]),
        ]
    };

    // Iterate over cubes (one fewer than grid points in each dim)
    let xi_max = if nx > 0 { nx - 1 } else { 0 };
    let yi_max = if ny > 0 { ny - 1 } else { 0 };
    let zi_max = if nz > 0 { nz - 1 } else { 0 };

    for i in 0..xi_max {
        for j in 0..yi_max {
            for k in 0..zi_max {
                // 8 cube corners (Bourke ordering)
                let p: [[f32; 3]; 8] = [
                    pos(i, j, k),
                    pos(i + 1, j, k),
                    pos(i + 1, j + 1, k),
                    pos(i, j + 1, k),
                    pos(i, j, k + 1),
                    pos(i + 1, j, k + 1),
                    pos(i + 1, j + 1, k + 1),
                    pos(i, j + 1, k + 1),
                ];
                let v: [f32; 8] = [
                    sample(i, j, k),
                    sample(i + 1, j, k),
                    sample(i + 1, j + 1, k),
                    sample(i, j + 1, k),
                    sample(i, j, k + 1),
                    sample(i + 1, j, k + 1),
                    sample(i + 1, j + 1, k + 1),
                    sample(i, j + 1, k + 1),
                ];

                // Cube index
                let mut cube_idx: usize = 0;
                for (b, vb) in v.iter().enumerate() {
                    if *vb >= iso {
                        cube_idx |= 1 << b;
                    }
                }

                let edge_flags = EDGE_TABLE[cube_idx];
                if edge_flags == 0 {
                    continue;
                }

                // Compute the 12 possible edge intersection vertices
                let mut edge_verts = [[0.0f32; 3]; 12];
                if edge_flags & 0x001 != 0 {
                    edge_verts[0] = interp(p[0], p[1], v[0], v[1]);
                }
                if edge_flags & 0x002 != 0 {
                    edge_verts[1] = interp(p[1], p[2], v[1], v[2]);
                }
                if edge_flags & 0x004 != 0 {
                    edge_verts[2] = interp(p[2], p[3], v[2], v[3]);
                }
                if edge_flags & 0x008 != 0 {
                    edge_verts[3] = interp(p[3], p[0], v[3], v[0]);
                }
                if edge_flags & 0x010 != 0 {
                    edge_verts[4] = interp(p[4], p[5], v[4], v[5]);
                }
                if edge_flags & 0x020 != 0 {
                    edge_verts[5] = interp(p[5], p[6], v[5], v[6]);
                }
                if edge_flags & 0x040 != 0 {
                    edge_verts[6] = interp(p[6], p[7], v[6], v[7]);
                }
                if edge_flags & 0x080 != 0 {
                    edge_verts[7] = interp(p[7], p[4], v[7], v[4]);
                }
                if edge_flags & 0x100 != 0 {
                    edge_verts[8] = interp(p[0], p[4], v[0], v[4]);
                }
                if edge_flags & 0x200 != 0 {
                    edge_verts[9] = interp(p[1], p[5], v[1], v[5]);
                }
                if edge_flags & 0x400 != 0 {
                    edge_verts[10] = interp(p[2], p[6], v[2], v[6]);
                }
                if edge_flags & 0x800 != 0 {
                    edge_verts[11] = interp(p[3], p[7], v[3], v[7]);
                }

                // Emit triangles
                let tri_table_row = &TRI_TABLE[cube_idx];
                let base = verts.len() as u32;
                let mut local_count = 0u32;
                let mut ti = 0;
                while ti < 16 {
                    let e0 = tri_table_row[ti];
                    if e0 == -1 {
                        break;
                    }
                    let e1 = tri_table_row[ti + 1];
                    let e2 = tri_table_row[ti + 2];
                    verts.push(edge_verts[e0 as usize]);
                    verts.push(edge_verts[e1 as usize]);
                    verts.push(edge_verts[e2 as usize]);
                    tris.push([
                        base + local_count,
                        base + local_count + 1,
                        base + local_count + 2,
                    ]);
                    local_count += 3;
                    ti += 3;
                }
            }
        }
    }

    Mesh { verts, tris }
}

